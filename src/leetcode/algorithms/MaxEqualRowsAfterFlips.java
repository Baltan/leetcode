    public static int maxEqualRowsAfterFlips(int[][] matrix) {
        int result = 0;
        int rows = matrix.length;
        int cols = matrix[0].length;
        Map<String, Integer> map = new HashMap<>();

        for (int i = 0; i < rows; i++) {
            String str = Arrays.toString(matrix[i]);

            if (!map.containsKey(str)) {
                String com = Arrays.toString(complementary(matrix[i], cols));

                if (map.containsKey(com)) {
                    int count = map.get(com) + 1;
                    map.put(com, count);
                    result = Math.max(result, count);
                } else {
                    int count = map.getOrDefault(str, 0) + 1;
                    map.put(str, count);
                    result = Math.max(result, count);
                }
            } else {
                int count = map.get(str) + 1;
                map.put(str, count);
                result = Math.max(result, count);
            }
        }
        return result;
    }

    public static int[] complementary(int[] arr, int cols) {
        for (int i = 0; i < cols; i++) {
            arr[i] = 1 - arr[i];
        }
        return arr;
    }
