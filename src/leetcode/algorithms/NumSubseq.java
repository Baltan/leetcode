    /**
     * 参考：
     * <a href="https://leetcode-cn.com/problems/number-of-subsequences-that-satisfy-the-given-sum-condition/solution/man-zu-tiao-jian-de-zi-xu-lie-shu-mu-by-leetcode-s/"></a>
     *
     * @param nums
     * @param target
     * @return
     */
    public static int numSubseq(int[] nums, int target) {
        int result = 0;
        int mod = 1000000007;
        int length = nums.length;
        int lo = 0;
        int hi = length - 1;
        /**
         * help[i]表示Math.pow(2,i)%1000000007的值
         */
        int[] help = new int[length + 1];
        Arrays.sort(nums);
        help[0] = 1;

        for (int i = 1; i <= length; i++) {
            /**
             * 如果直接计算Math.pow(2,i)%1000000007的值可能会因为Math.pow(2,i)溢出导出错误，必须从
             * Math.pow(2,i-1)%1000000007的值递推出Math.pow(2,i)%1000000007的值
             */
            help[i] = help[i - 1] * 2 % mod;
        }
        /**
         * 双指针
         */
        while (lo <= hi) {
            /**
             * 如果nums[lo]+nums[hi]<=target，保证选择nums[lo]的情况下，nums[lo+1]-nums[hi]这hi-lo个数每个数
             * 都可以选择或者不选择，共有Math.pow(2,hi-lo)种情况；如果nums[lo]+nums[hi]>target，则说明子数组中
             * 不可能同时出现nums[lo]和nums[hi]，必须缩小子数组中的最大值，将hi指针左移
             */
            if (nums[lo] + nums[hi] > target) {
                hi--;
            } else {
                result = (result + help[hi - lo]) % mod;
                lo++;
            }
        }
        return result % mod;
    }
