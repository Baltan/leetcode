    public static int threeSumMulti(int[] A, int target) {
        long result = 0L;
        List<List<Integer>> resultList = new LinkedList<>();
        int[] count = new int[101];
        Set<Integer> numSet = new HashSet<>();
        List<Integer> numList;
        List<Integer> temp = new ArrayList<>();
        /**
         * 统计每个数字出现的次数，并且将每种数字加入numSet中用于后面讨论A、B、C三个数字都不相等的情况
         */
        for (int num : A) {
            numSet.add(num);
            count[num]++;
        }

        numList = new ArrayList<>(numSet);
        Collections.sort(numList);
        dfs(resultList, numList, temp, 0, target);
        /**
         * 计算A、B、C可能数
         */
        for (List<Integer> list : resultList) {
            result += count[list.get(0)] * count[list.get(1)] * count[list.get(2)];
        }
        /**
         * 计算A、A、B可能数
         */
        for (int num : numList) {
            int num1 = target - num * 2;

            if (count[num] >= 2 && num != num1 && num1 >= 0 && num1 <= 100 && count[num1] > 0) {
                result += count[num] * (count[num] - 1) / 2 * count[num1];
            }
        }
        /**
         * 计算A、A、A可能数
         */
        if (target % 3 == 0 && count[target / 3] >= 3) {
            int num = target / 3;
            result += 1L * count[num] * (count[num] - 1) * (count[num] - 2) / 6;
        }
        return (int) (result % 1000000007);
    }

    public static void dfs(List<List<Integer>> resultList, List<Integer> numList, List<Integer> temp, int sum
            , int target) {
        if (sum == target && temp.size() == 3) {
            resultList.add(new ArrayList<>(temp));
            return;
        }

        if (sum > target) {
            return;
        }

        if (temp.size() == 3 && sum != target) {
            return;
        }

        int size = numList.size();

        for (int i = 0; i < size; i++) {
            int num = numList.get(i);

            if (!temp.isEmpty() && num < temp.get(temp.size() - 1)) {
                continue;
            }

            temp.add(num);
            numList.remove(new Integer(num));
            dfs(resultList, numList, temp, sum + num, target);
            numList.add(i, num);
            temp.remove(temp.size() - 1);
        }
    }
