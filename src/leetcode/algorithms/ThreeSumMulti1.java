    public static int threeSumMulti(int[] A, int target) {
        long result = 0L;
        int[] count = new int[101];
        /**
         * 统计每个数字出现的次数
         */
        for (int num : A) {
            count[num]++;
        }

        for (int i = 0; i < 101; i++) {
            for (int j = 0; j < 101; j++) {
                int k = target - i - j;

                if (k < 0 || k > 100) {
                    continue;
                }

                if (i == j && j == k && count[i] >= 3) {
                    /**
                     * 计算A、A、A可能数
                     */
                    result += 1L * count[i] * (count[i] - 1) * (count[i] - 2) / 6;
                } else if (i == j && i != k && count[i] >= 2 && count[k] >= 1) {
                    /**
                     * 计算A、A、B可能数
                     */
                    result += 1L * count[i] * (count[i] - 1) / 2 * count[k];
                } else if (i < j && j < k && count[i] > 0 && count[j] > 0 && count[k] > 0) {
                    /**
                     * 计算A、B、C可能数
                     */
                    result += 1L * count[i] * count[j] * count[k];
                }
            }
        }
        return (int) (result % 1000000007);
    }
