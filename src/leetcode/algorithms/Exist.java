    private static boolean result = false;

    public static boolean exist(char[][] board, String word) {
        /**
         * 因为把result定义成了static变量，为保证每个测试用例result的初始值都为false
         */
        result = false;
        int length = board.length;
        int width = board[0].length;

        for (int i = 0; i < length; i++) {
            for (int j = 0; j < width; j++) {
                if (board[i][j] == word.charAt(0)) {
                    boolean[][] book = new boolean[length][width];
                    book[i][j] = true;
                    dfs(board, book, word.substring(1), i, j);

                    if (result) {
                        return true;
                    }

                }
            }
        }
        return false;
    }

    public static void dfs(char[][] board, boolean[][] book, String word, int startX, int startY) {
        if (word.length() == 0) {
            result = true;
            return;
        }

        int length = board.length;
        int width = board[0].length;

        if (startX - 1 >= 0 && board[startX - 1][startY] == word.charAt(0) && !book[startX - 1][startY] &&
                !result) {
            book[startX - 1][startY] = true;
            dfs(board, book, word.substring(1), startX - 1, startY);
            book[startX - 1][startY] = false;
        }
        if (startX + 1 < length && board[startX + 1][startY] == word.charAt(0) && !book[startX + 1][startY] &&
                !result) {
            book[startX + 1][startY] = true;
            dfs(board, book, word.substring(1), startX + 1, startY);
            book[startX + 1][startY] = false;
        }
        if (startY - 1 >= 0 && board[startX][startY - 1] == word.charAt(0) && !book[startX][startY - 1] &&
                !result) {
            book[startX][startY - 1] = true;
            dfs(board, book, word.substring(1), startX, startY - 1);
            book[startX][startY - 1] = false;
        }
        if (startY + 1 < width && board[startX][startY + 1] == word.charAt(0) && !book[startX][startY + 1] &&
                !result) {
            book[startX][startY + 1] = true;
            dfs(board, book, word.substring(1), startX, startY + 1);
            book[startX][startY + 1] = false;
        }
    }
