    private static boolean flag = false;

    public static boolean exist(char[][] board, String word) {
        /**
         * 因为把result定义成了static变量，为保证每个测试用例result的初始值都为false
         */
        flag = false;
        int rows = board.length;
        int cols = board[0].length;
        char[] charArray = word.toCharArray();

        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < cols; j++) {
                if (board[i][j] == charArray[0]) {
                    /**
                     * 标记board中的字符是否已被使用过
                     */
                    boolean[][] isVisited = new boolean[rows][cols];
                    isVisited[i][j] = true;
                    /**
                     * 从charArray[1]开始继续向下查找
                     */
                    dfs(board, isVisited, charArray, 1, i, j);
                    /**
                     * 如果已经在board中找到了单词，直接返回true
                     */
                    if (flag) {
                        return true;
                    }

                }
            }
        }
        return false;
    }

    /**
     * @param board
     * @param isVisited 标记board[row][col]这个字符是否已被使用过
     * @param charArray
     * @param index     当前要匹配的字符为charArray[index]
     * @param row
     * @param col
     */
    public static void dfs(char[][] board, boolean[][] isVisited, char[] charArray, int index, int row,
                           int col) {
        /**
         * 已在board中找到了charArray中的所有字符，直接返回true
         */
        if (index == charArray.length) {
            flag = true;
            return;
        }

        int rows = board.length;
        int cols = board[0].length;

        if (row - 1 >= 0 && board[row - 1][col] == charArray[index] && !isVisited[row - 1][col] && !flag) {
            isVisited[row - 1][col] = true;
            dfs(board, isVisited, charArray, index + 1, row - 1, col);
            isVisited[row - 1][col] = false;
        }

        if (row + 1 < rows && board[row + 1][col] == charArray[index] && !isVisited[row + 1][col] &&
                !flag) {
            isVisited[row + 1][col] = true;
            dfs(board, isVisited, charArray, index + 1, row + 1, col);
            isVisited[row + 1][col] = false;
        }

        if (col - 1 >= 0 && board[row][col - 1] == charArray[index] && !isVisited[row][col - 1] &&
                !flag) {
            isVisited[row][col - 1] = true;
            dfs(board, isVisited, charArray, index + 1, row, col - 1);
            isVisited[row][col - 1] = false;
        }

        if (col + 1 < cols && board[row][col + 1] == charArray[index] && !isVisited[row][col + 1] &&
                !flag) {
            isVisited[row][col + 1] = true;
            dfs(board, isVisited, charArray, index + 1, row, col + 1);
            isVisited[row][col + 1] = false;
        }
    }
